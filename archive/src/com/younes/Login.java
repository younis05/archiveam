    private void jshowMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jshowMouseClicked
       if(jhide.getText().equals("Hide")){
            ImageIcon eye=new ImageIcon(getClass().getResource("/images/icons8_eye_30px.png")) ;
            jshow.setIcon(eye);
            jpassword.setEchoChar('*');
            jhide.setText("Show");
       }else if(jhide.getText().equals("Show")){
          ImageIcon hide=new ImageIcon(getClass().getResource("/images/icons8_hide_30px.png")) ;
            jshow.setIcon(hide);
            
            jpassword.setEchoChar((char)0);
            jhide.setText("Hide");
       }
        
    }//GEN-LAST:event_jshowMouseClicked

