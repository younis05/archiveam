   public boolean verifyFields(){
       boolean verify=false;
       String username= jusername.getText();
       JTextField txtpass=(JTextField)jpassword;
       String password=txtpass.getText() ;
       if(username.trim().isEmpty()||password.trim().isEmpty()){
           return true;
       }else{
           return false;
       }
   }
    public void loginUser(){
        JTextField txtpass=(JTextField)jpassword;
       String password=txtpass.getText() ;
        try{
            if(verifyFields()) {
           JOptionPane.showMessageDialog(this,"Fill Fields...!", "Warning!", JOptionPane.ERROR_MESSAGE);
       }else{
               String sql="SELECT * FROM `user` WHERE  `username`=? and `password`=?";
              pst=conn.prepareStatement(sql);
              pst.setString(1, jusername.getText());
              pst.setString(2, password);
              rs=pst.executeQuery();
              if(rs.next()){
                  this.dispose();
                  home=new Home();
                  home.setVisible(true);
                  pst.close();rs.close();
                  System.out.println(" login success!"); 
              }else{
                  d++;
                  if(d==3){
                      System.exit(0);
                  }
                  JOptionPane.showMessageDialog(this,"Your username or password incorrect...!", "Warning!", JOptionPane.ERROR_MESSAGE);
              }
           
       }
        }catch(Exception ex){
            
            JOptionPane.showMessageDialog(this,ex.getMessage());
        }
    }
    
