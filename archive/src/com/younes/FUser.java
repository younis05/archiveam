    private void showAllUsers(){
        DefaultTableModel table=new DefaultTableModel();
        table.addColumn("NÂ°");table.addColumn("User Name");table.addColumn("Password");
        
        String sql="SELECT * FROM `user` ORDER BY `username` ASC";
        try {
            pst=conn.prepareStatement(sql);
            rst=pst.executeQuery();
            while(rst.next()){
                table.addRow(new Object[]{rst.getString(1),rst.getString(2),rst.getString(3)});
            }
            jtableuser.setModel(table);
            jtableuser.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
            jtableuser.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
            for(int i=0;i<jtableuser.getColumnCount();i++){
                 jtableuser.getColumnModel().getColumn(i).setCellRenderer(tblcenter);
             }
        } catch (Exception ex) {
           ex.printStackTrace();
        }
        
    }
