    private void showInfoUsers(){
        int row=jtableuser.getSelectedRow();
        if(row==-1){
         JOptionPane.showMessageDialog(null,"choose row...!");
        }else{
        String Table_Click=(jtableuser.getModel().getValueAt(row, 0)).toString();
        int p_id=Integer.parseInt(Table_Click);
        User user=UserService.getUser(p_id);
        jid.setText(String.valueOf(user.getId()));
        jusername.setText(user.getUsername());
        jpassword.setText(user.getPassword());
        
        }
    } 
    
    
